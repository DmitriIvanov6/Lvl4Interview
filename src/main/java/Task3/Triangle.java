package Task3;

public class Triangle{

    private double side;
    private double h;

    public Triangle (double side, double h) {
        this.side = side;
        this.h = h;
    }

    public double getSide() {
        return side;
    }

    public void setSide(double side) {
        this.side = side;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }
}
